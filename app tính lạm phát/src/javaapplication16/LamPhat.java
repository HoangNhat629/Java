    private void UPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UPDATEActionPerformed
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        if (Table.getSelectedRow() == -1) {
            if (Table.getRowCount() == 0) {
                MESS.setText("Table is empty");
            } else {
                MESS.setText("You must select a index");
            }
        } else {

            model.setValueAt(CHISOLAMPHAT.getText(), Table.getSelectedRow(), 0);
            model.setValueAt(DANHGIABANDAU.getText(), Table.getSelectedRow(), 1);
            model.setValueAt(DANHGIACHUYENGIA.getText(), Table.getSelectedRow(), 2);

        }
    }//GEN-LAST:event_UPDATEActionPerformed

