    private void jLabel37MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel37MouseClicked
        float a = Float.parseFloat(HOWAWA.getText());
        float b = Float.parseFloat(CHISOBANDAU.getText());
        float c = a - b;

        if (c > 0) {
            if (c > 0.1) {
                JOptionPane.showMessageDialog(this, "The results show that there is galloping inflation (unexpected case).");
            } else {
                if (0.02 <= c && c <= 0.05) {
                    JOptionPane.showMessageDialog(this, "The results show that inflation is stable");
                } else {
                    JOptionPane.showMessageDialog(this, "The results show that inflation is expected");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "The results show that deflation");
        }
    }//GEN-LAST:event_jLabel37MouseClicked

